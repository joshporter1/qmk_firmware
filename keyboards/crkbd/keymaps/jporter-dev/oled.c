bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    static uint16_t my_hash_timer;

    // keylogger
    if (record->event.pressed) {
        add_keylog(keycode);
    }
    // pet stuff
    switch(keycode){
        /* KEYBOARD PET STATUS START */
        case OSM(MOD_LSFT):
        case OSM(MOD_RSFT):
            if (record->event.pressed){
                isBarking = true;
            } else {
                isBarking = false;
            }
            break;
        case LCTL_T(KC_SPC):
            if (record->event.pressed) {
                my_hash_timer = timer_read();

                isSneaking = true;
            } else {
                if (timer_elapsed(my_hash_timer) < TAPPING_TERM) {
                    isSneaking = false;
                    isJumping = true;
                    showedJump = false;
                } else {
                    isJumping = false;
                    isSneaking = false;
                }
                isJumping = false;
            }
            break;
        /* KEYBOARD PET STATUS END */
    }
    return true;
}
